 package com.shadab.demo.bean;

public class MobileBean {
	
private float mobilePrice;
private int mobileHeight;
private int mobileWeight;
private String mobileCompany;
private String mobileModel;
public float getMobilePrice() {
	return mobilePrice;
}
public void setMobilePrice(float mobilePrice) {
	this.mobilePrice = mobilePrice;
}
public int getMobileHeight() {
	return mobileHeight;
}
public void setMobileHeight(int mobileHeight) {
	this.mobileHeight = mobileHeight;
}
public int getMobileWeight() {
	return mobileWeight;
}
public void setMobileWeight(int mobileWeight) {
	this.mobileWeight = mobileWeight;
}
public String getMobileCompany() {
	return mobileCompany;
}
public void setMobileCompany(String mobileCompany) {
	this.mobileCompany = mobileCompany;
}
public String getMobileModel() {
	return mobileModel;
}
public void setMobileModel(String mobileModel) {
	this.mobileModel = mobileModel;
}


}
